        <li>
        	<p><b>My Video Background Slider not displayed on mobile devices. Why?</b></p>
        	<p>Since iOS won't auto-play video on mobile you will have to add a fallback photo to be displayed instead of the video on mobile devices. To do that, go to Appearance > Customize > Frontpage > Slider > Static Slide Settings and Click the "Image" tab and upload a fallback image.</p>
        </li>
    
        <li>
